* Números Reais

- O conjunto dos números Reais é representado por: R

- Reta Real:
- ---------------|============|--------|------ +   <=>  (a < b < 0) =  [a, b]
                 a            b        0
              (conteúdo de a à b)  - <- -> +

- ---------]============|===|=======[--------- +  =  ]c − r, c + r[
         c - r          c   x     c + r

- Módulo: 
|x| = {  x se x≥0
      { −x se x<0

- O conjunto dos números Naturais é representado por: N
(1, 2, 3, ...)

- O conjunto dos números Inteiros é representado por: Z
(..., -2, -1, 0, 1, 2, ...)

- O conjunto dos números RAcionais é representado por: Q
(..., 1/2, 2/7, 7/9, ...)


- Conjuntos Limitados:       ---> Exemplo: { xeR| 3 < x <= 8 }
                             -----------|------|-------|----->
                                        0      3       8
                                               ]-------]
Majorantes (Maj): [8, +infinito[
Minorantes (Min): ]-infinito, 3[
Máximo (MAX)    :  8 (faz parte)
Mínimo (MIN)    :  NA (3 não faz parte)
Supremo (Sup)   :  8 
Ínfimo (Inf)    :  3

- Estrutura Topológica:
Conjunto Aberto: [0,3[ \ {1}U{4}
Conjunto Fechado: [0,3]

- Ponto de Acumulação:

%Exemplo:
x = ]-2,2]{4}
(-2, 1, 0, 1, 2): É ponto de acumulação.
(4): Não é ponto de acumulação, é ponto isolado.