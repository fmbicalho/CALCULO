* Funções

f(função) : D(domínio) ------> E(conjunto de chegada)

- Função: f(x)
- Imagem: Im(x)
- Contra-Domínio: CD(f)

- Tipos de Funções:

|x1| |y1|
|x2| |y2|
|x3| |y3|

Injetiva:    x1,x2,x3 /= y1,y2,y3
Sobrejetiva: x1,x2,x3 =  y1,y2,y3
Bijetiva:    todos os xs ligados a todos os y

- Funções Reais: Majorada:    f(x) <= m
                 Minorada:    f(x) >= m
                 Limitada:    m <= f(x) <= n
                 Crescente:   f(x1) <= f(x2)
                 Decrescente: f(x1) >= f(x2)
                 Par:         gráfico simétrico
                 Ímpar:       gráfico simétrico mas invertido

- Funções Compostas: (g composta cpm f, "gof")
gof: X ---> W
     x |--> g(f(x))

- Extremos:

Máximo local: vários pontos máximos na curva.
Mínimo local: vários pontos mínimos na curva.
Máximo absoluto: ponto mais alto da curva.
Mínimo absoluto: ponto mais baixo da curva.