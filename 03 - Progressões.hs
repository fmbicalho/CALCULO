* Progressões

- Progressão Aritmética: Sempre Constante.

r>0, r<0, r=0

Fórmula Geral: Un = U1 + (n-1).r

%Exemplo: r=(-7), U1 = 18
          Un = 18 + (n-1).(-7)
          Un = 25 - 7n

Para calcular os n primeiros elementos:

U1+Un
----- . n
  2

- Progressão Geométrica: Não Constante.

Fórmula Geral: Un + 1 = Un.r

Se r > 1: Monótona Crescente ou Decrescente.
   r = 1: Monótona Crescente.
   r = 0: Nula.
   r < 0: Não Monótona.
   0 < r < 1: Monótona Crescente ou Decrescente.
   U1 = 0: Nula.

Para calcular os n primeiros elementos:

     1 - r^n
U1 . -------
      1 - r