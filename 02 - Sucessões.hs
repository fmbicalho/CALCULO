* Sucessões 

- Tipos: - Crescente: Estritamente crescente
		 - Decrescente: Estritamente decrescente
		 - Monótona: Cresce ou Decresce
		 - Limitada: - Inferiormente: "[a", +infinito]
		 		     - Superiormente: [-infinito, "a]"

* Séries E

- Série Gemométrica: E N>=1, r^N-1
- Série Harmônica:   E N e N, 1/N
- Série de Riemann:  E N e N, 1/N^infinito
- Séries Alternadas: - Leibniz: E (-1)^n an -> Convergente 
					 - Cauchy:  E un, l>1 -> Divergente
					                  l<1 -> Convergente 